# Custom ASGI Middleware to strictly satisfy Chrome's Private Network Access (PNA) Policy.
# This attaches the 'Access-Control-Allow-Private-Network: true' header to all responses,
# including OPTIONS preflight responses generated by CORSMiddleware.
class PNAMiddleware:
    def __init__(self, app):
        self.app = app

    async def __call__(self, scope, receive, send):
        if scope["type"] != "http":
            return await self.app(scope, receive, send)

        async def send_wrapper(message):
            if message["type"] == "http.response.start":
                headers = message.setdefault("headers", [])
                headers.append((b"access-control-allow-private-network", b"true"))
            await send(message)

        await self.app(scope, receive, send_wrapper)


app.add_middleware(PNAMiddleware)

